    private void btnIngresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresoActionPerformed
        
        String usuario = txtUsuario.getText();
        String contrasenia = txtContrasenia.getText();
        Usuario usuar = control.validarUsuario(usuario, contrasenia);
        
        if (usuar != null){
            String rol = usuar.getUnRol().getNombreRol();
            if (rol.equals("admin")) {
                PrincipalAdmin pAdmin = new PrincipalAdmin(control,usuar);
                pAdmin.setVisible(true);
                pAdmin.setLocationRelativeTo(null);
                this.dispose();
            }
            if (rol.equals("usuario")) {
                PrincipalUsuario pUsuario = new PrincipalUsuario(control,usuar);
                pUsuario.setVisible(true);
                pUsuario.setLocationRelativeTo(null);
                this.dispose();
            }
        }else {
        txtMensaje.setText("El usuario o contraseña con incorrectos"); 
        }
        
        
        // TODO add your handling code here:
    }//GEN-LAST:event_btnIngresoActionPerformed

    
